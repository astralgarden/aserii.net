  <h2>I hope I dont bore you with a story...</h2>
  <span style="color:#000000";>
 <p>I'm not really sure how you got here, its entirely possible you're a friend checking up on this website, or a google searchbot, or just a random person who stumbled across this. 
 But I'm kinda glad you're here. I wanna tell the story of aserii.net if thats alright.
 </p>
 <p>
 So, the year 2020, I think most people remember. Obviously, there was a pandemic, but that year was even harder for me, as I was struggling with a lot of mental health issues. 
 Around that year, I spent a lot of time online, making new friends and seeing new things. I discovered a lot of websites made by people just for fun. I had made little websites before, like on wix or squarespace, for fun, but I had never seen someone make a full on website just for fun that they coded themselves.
 I thought that seemed super cool so I spent a month on making a pokemon fansite! It was really bad but the experience I got was an important step, plus I had a lot of fun with it. 
 </p>
 <p>That didn't work out great and I didn't keep that forever, but the skills I learned stayed with me. Later in the year, I made another website: Just a website where I could kind of do whatever I wanted. I called it Aserii.net (Aserii translates to 'Stars'). That website ended up being a place where I discovered myself, and I had a lot of fun making it. 
 2021 was when I learned that I was apart of a Dissociative Identity Disorder system (technically I didn't know it was DID at the time but whatever). The stress of figuring that out while at the time, being abused, was a lot, and I was not at all in a good mental place. I decided that once I was an adult, I'd publish this website I was making. I wanted to make it as good as possible. I'll admit, that website was the only thing keeping me alive for a few months.</p>
 <p>For a number of reason, the website didn't survive. The main version of it was deleted and all thats left of the original is an outdated backup. Time went by, aserii.net got ignored. A lot of bad things happened.</p>
 <p>Yet here I am. aserii.net is now a registered domain by yours truly. This website is being rebuilt from the ground up. I'm not an adult yet, but we're no longer attending school so we can take time to recover our system's mental health. My mental health issues have been diagnosed and are being treated, and I'm in a better place now, and for once there's some hope in my life for a better future :)</p>
 <p>I want aserii.net to have a similar goal to the original. I want it to be the website we work on while we recover. My headmates too, not just me. I want it to be like the best group project ever. I just want to be able to put whatever I want on this website because I can. I think thats kinda beautiful.</p>
 <p>Needless to say, this website has a lot of importance to me. I plan on making it into something that I'm proud of soon. Thanks for reading.</p><br>
